package contando_estrutura_for;

public class contando_estrutura_for {

	public static void main(String[] args) {
		
		System.out.println("Decrescente");
		for(int i=10; i >= 0; i--) {
			System.out.println(i);
		}
		
		System.out.println("Crescente");
		for(int i=0; i <= 10; i++) {
			System.out.println(i);
		}

	}

}
